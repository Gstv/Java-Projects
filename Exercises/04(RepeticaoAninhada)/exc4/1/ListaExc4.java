public class ListaExc4{
  public static void main (String args []){
    
    int repetLinha=0, repetAsterist=0;
    
    //PROCESSAMENTO
    
    while(10 > repetLinha){
      
      while(10 > repetAsterist){
        System.out.print(repetAsterist+ " ");
        repetAsterist++;
      }
      System.out.println();
      repetAsterist=0;
      repetLinha++;
    }
  }
}
    
    