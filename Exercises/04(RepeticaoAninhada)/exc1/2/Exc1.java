package lista4;

public class Exc1 {
    public static void main(String[] args) {
        
        int i=0, j;
        
        while(i < 10){
            j=0;
            while(j < 10){
                System.out.print("*  ");
                j++;
            }
            
            System.out.println();
            i++;
        }
    }
    
}
